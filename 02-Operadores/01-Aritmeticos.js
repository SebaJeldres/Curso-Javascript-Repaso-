let a = 5
let b = 2

console.log(a + b, "suma") // Suma
console.log(a - b, "resta") // Resta
console.log(a * b, "multplicacion") // Multiplicación
console.log(a / b, "Division") // División
console.log(a % b, "Resto") // Módulo (residuo)
console.log(a ** b, "Potencia") // Exponente (potencia)


// Incremento
console.log(++a) // incremento
console.log(a)

//decremento
console.log(--b) // decremento
console.log(b)